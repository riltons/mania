// Função de teste simples sem autenticação para verificar problemas de CORS

// Cabeçalhos CORS para permitir requisições de qualquer origem
const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

Deno.serve(async (req) => {
  // Lidar com requisições OPTIONS (preflight)
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders });
  }
  
  try {
    // Resposta simples para qualquer método
    return new Response(
      JSON.stringify({
        success: true,
        message: 'Função de teste funcionando!',
        timestamp: new Date().toISOString(),
        method: req.method
      }),
      { 
        status: 200, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );
  } catch (err) {
    console.error('Erro na função de teste:', err);
    return new Response(
      JSON.stringify({ success: false, error: err.message || 'Erro interno' }),
      { 
        status: 500, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );
  }
})

